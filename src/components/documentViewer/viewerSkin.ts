function isLightPreset(preset: ThemePresetId) {
  return preset === 'ghostty-light';
}

export function resolveViewerSkin(
  family: DocumentLanguageFamily,
  themePreset: ThemePresetId,
): ViewerSkinTokens {
  const base = DARK_SKINS[family] ?? DARK_SKINS.generic;
  const overrides = isLightPreset(themePreset) ? (LIGHT_OVERRIDES[family] ?? LIGHT_OVERRIDES.generic) : undefined;

  return {
    family,
    ...base,
    ...overrides,
  };
}

